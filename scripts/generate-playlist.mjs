// Reads each <SOURCE>/<folder>/info.txt + an .mp4 file in that folder, and
// writes a generated TypeScript module that the rest of the app imports as
// the PLAYLIST. Runs as a pre-step before `next dev` and `next build`.
//
// Two source folders are merged into one playlist, each entry tagged with
// the `set` it came from so the UI can toggle between sets:
//   public/submissions          -> set "one"
//   public/submission set two   -> set "two"
//
// info.txt is plain text with one "Key: Value" per line, optionally
// surrounded by separator lines like "-------------------".
// Recognised keys: Name, Title, Category, Email, Instagram, Submitted At, Source URL.

import fs from "node:fs";
import path from "node:path";

const SOURCES = [
  { set: "one", dir: "public/submissions", urlPrefix: "/submissions" },
  {
    set: "two",
    dir: "public/submission set two",
    urlPrefix: `/${encodeURIComponent("submission set two")}`,
  },
];
const OUT = "src/lib/playlist.generated.ts";

function parseInfoTxt(text) {
  const out = {};
  for (const raw of text.split(/\r?\n/)) {
    const line = raw.trim();
    if (!line || /^-{3,}$/.test(line)) continue;
    const idx = line.indexOf(":");
    if (idx === -1) continue;
    const key = line.slice(0, idx).trim();
    const value = line.slice(idx + 1).trim();
    if (key) out[key] = value;
  }
  return out;
}

function pickVideoFile(dir) {
  if (!fs.existsSync(dir)) return null;
  const files = fs.readdirSync(dir);
  const mp4 = files.find((f) => /\.mp4$/i.test(f));
  return mp4 ?? null;
}

const seenIds = new Set();
const playlist = [];

for (const source of SOURCES) {
  if (!fs.existsSync(source.dir)) {
    console.warn(`[playlist] ${source.dir} does not exist; skipping set "${source.set}"`);
    continue;
  }

  const folders = fs
    .readdirSync(source.dir, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort();

  for (const folder of folders) {
    const dir = path.join(source.dir, folder);
    const infoPath = path.join(dir, "info.txt");
    const videoFile = pickVideoFile(dir);

    if (!fs.existsSync(infoPath)) {
      console.warn(`[playlist] ${source.dir}/${folder}: missing info.txt, skipping`);
      continue;
    }
    if (!videoFile) {
      console.warn(`[playlist] ${source.dir}/${folder}: no .mp4 in folder, skipping`);
      continue;
    }
    const info = parseInfoTxt(fs.readFileSync(infoPath, "utf8"));
    const title = (info.Title || "").trim();
    if (!title) {
      console.warn(`[playlist] ${source.dir}/${folder}: info.txt missing Title, skipping`);
      continue;
    }
    // Folder names collide across sets only by accident; if they do, namespace
    // the set-two id to keep ratings isolated.
    let id = folder;
    if (seenIds.has(id)) id = `set2:${folder}`;
    if (seenIds.has(id)) {
      console.warn(`[playlist] ${source.dir}/${folder}: duplicate id "${id}", skipping`);
      continue;
    }
    seenIds.add(id);
    const creator =
      (info.Instagram && (info.Instagram.startsWith("@") ? info.Instagram : `@${info.Instagram}`)) ||
      info.Name ||
      null;
    playlist.push({
      id,
      title,
      creator,
      set: source.set,
      url: `${source.urlPrefix}/${encodeURIComponent(folder)}/${encodeURIComponent(videoFile)}`,
    });
  }
}

const banner = `// AUTO-GENERATED by scripts/generate-playlist.mjs. Do not edit.\n`;
const body = `import type { Video } from "./videos";\n\nexport const PLAYLIST: Video[] = ${JSON.stringify(
  playlist,
  null,
  2
)};\n`;
fs.writeFileSync(OUT, banner + body);
console.log(
  `[playlist] wrote ${playlist.length} entries to ${OUT}` +
    ` (${playlist.filter((v) => v.set === "one").length} in set one, ${playlist.filter((v) => v.set === "two").length} in set two)`
);
