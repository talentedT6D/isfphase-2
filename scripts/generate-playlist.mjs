// Reads each public/submissions/<folder>/info.txt + an .mp4 file in that
// folder, and writes a generated TypeScript module that the rest of the
// app imports as the PLAYLIST. Runs as a pre-step before `next dev` and
// `next build`.
//
// info.txt is plain text with one "Key: Value" per line, optionally
// surrounded by separator lines like "-------------------".
// Recognised keys: Name, Title, Category, Email, Instagram, Submitted At, Source URL.

import fs from "node:fs";
import path from "node:path";

const SRC = "public/submissions";
const OUT = "src/lib/playlist.generated.ts";

function parseInfoTxt(text) {
  const out = {};
  for (const raw of text.split(/\r?\n/)) {
    const line = raw.trim();
    if (!line || /^-{3,}$/.test(line)) continue;
    const idx = line.indexOf(":");
    if (idx === -1) continue;
    const key = line.slice(0, idx).trim();
    const value = line.slice(idx + 1).trim();
    if (key) out[key] = value;
  }
  return out;
}

function pickVideoFile(folder) {
  const dir = path.join(SRC, folder);
  if (!fs.existsSync(dir)) return null;
  const files = fs.readdirSync(dir);
  const mp4 = files.find((f) => /\.mp4$/i.test(f));
  return mp4 ?? null;
}

function slugifyId(folder) {
  // Folder names are descriptive ("Adiyogi_The_Source_of_Yoga_Riya_Rathod").
  // Use them verbatim as ids — they're stable, unique per folder, and
  // safe for Supabase's text video_id.
  return folder;
}

if (!fs.existsSync(SRC)) {
  console.warn(`[playlist] ${SRC} does not exist; writing empty playlist`);
  fs.writeFileSync(
    OUT,
    `// AUTO-GENERATED by scripts/generate-playlist.mjs. Do not edit.\nimport type { Video } from "./videos";\nexport const PLAYLIST: Video[] = [];\n`
  );
  process.exit(0);
}

const folders = fs
  .readdirSync(SRC, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name)
  .sort();

const seenIds = new Set();
const playlist = folders.flatMap((folder) => {
  const dir = path.join(SRC, folder);
  const infoPath = path.join(dir, "info.txt");
  const videoFile = pickVideoFile(folder);

  if (!fs.existsSync(infoPath)) {
    console.warn(`[playlist] ${folder}: missing info.txt, skipping`);
    return [];
  }
  if (!videoFile) {
    console.warn(`[playlist] ${folder}: no .mp4 in folder, skipping`);
    return [];
  }
  const info = parseInfoTxt(fs.readFileSync(infoPath, "utf8"));
  const title = (info.Title || "").trim();
  if (!title) {
    console.warn(`[playlist] ${folder}: info.txt missing Title, skipping`);
    return [];
  }
  const id = slugifyId(folder);
  if (seenIds.has(id)) {
    console.warn(`[playlist] ${folder}: duplicate id "${id}", skipping`);
    return [];
  }
  seenIds.add(id);
  const creator =
    (info.Instagram && (info.Instagram.startsWith("@") ? info.Instagram : `@${info.Instagram}`)) ||
    info.Name ||
    null;
  return [
    {
      id,
      title,
      creator,
      url: `/submissions/${encodeURIComponent(folder)}/${encodeURIComponent(videoFile)}`,
    },
  ];
});

const banner = `// AUTO-GENERATED by scripts/generate-playlist.mjs. Do not edit.\n`;
const body = `import type { Video } from "./videos";\n\nexport const PLAYLIST: Video[] = ${JSON.stringify(
  playlist,
  null,
  2
)};\n`;
fs.writeFileSync(OUT, banner + body);
console.log(`[playlist] wrote ${playlist.length} entries to ${OUT}`);
